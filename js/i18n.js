let onLanguageChange = null;

export function setLanguageChangeHandler(fn) {
  onLanguageChange = fn;
}

export function getUiLanguage() {
  return getState().settings.uiLanguage || 'ru';
}

export function t(key, vars = {}, fallback) {
  const lang = getUiLanguage();
  let str = STRINGS[lang]?.[key] ?? STRINGS.ru[key] ?? fallback ?? key;
  Object.entries(vars).forEach(([k, v]) => {
    str = str.replaceAll(`{${k}}`, String(v));
  });
  return str;
}

export function setUiLanguage(lang) {
  updateSettings({ uiLanguage: lang });
  document.documentElement.lang = lang;
  applyDocumentI18n();
  onLanguageChange?.();
}

export function applyDocumentI18n() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    const attr = el.dataset.i18nAttr;
    const text = t(key);
    if (attr) el.setAttribute(attr, text);
    else el.textContent = text;
  });

  const search = document.getElementById('search-input');
  if (search) search.placeholder = t('search.placeholder');

  document.querySelectorAll('[data-user-hint]').forEach(hint => {
    if (!hint.dataset.userEmail) {
      hint.textContent = t('auth.guest');
    }
  });

  document.getElementById('reader-prev-chapter')?.replaceChildren(t('reader.prevChapter'));
  document.getElementById('reader-next-chapter')?.replaceChildren(t('reader.nextChapter'));
  document.getElementById('reader-prev')?.replaceChildren(t('reader.prevPage'));
  document.getElementById('reader-next')?.replaceChildren(t('reader.nextPage'));
  document.getElementById('reader-autoscroll')?.setAttribute('title', t('reader.autoscroll'));
}

