export function normalizeTitleKey(text) {
  return String(text || '')
    .toLowerCase()
    .normalize('NFKD')
    .replace(/[\u0300-\u036f]/g, '')
    .replace(/[^\p{L}\p{N}\s]/gu, ' ')
    .replace(/\s+/g, ' ')
    .trim();
}

export function lookupTitle(text, mangaId) {
  if (!text?.trim()) return null;
  if (mangaId && TITLE_BY_ID[mangaId]?.ru) return TITLE_BY_ID[mangaId].ru;
  const key = normalizeTitleKey(text);
  if (TITLE_ALIASES[key]) return TITLE_ALIASES[key];
  for (const [alias, ru] of Object.entries(TITLE_ALIASES)) {
    if (key.includes(alias) || alias.includes(key)) return ru;
  }
  if (mangaId && TITLE_BY_ID[mangaId]) {
    for (const alias of TITLE_BY_ID[mangaId].aliases || []) {
      if (normalizeTitleKey(alias) === key) return TITLE_BY_ID[mangaId].ru;
    }
  }
  return null;
}

export function translateChapterTitleLocal(title) {
  if (!title?.trim()) return title;
  const trimmed = title.trim();
  for (const [re, fn] of CHAPTER_TITLE_PATTERNS) {
    const m = trimmed.match(re);
    if (m) return fn(m);
  }
  if (/^(\d+(?:\.\d+)?)$/.test(trimmed)) return `Глава ${trimmed}`;
  return null;
}

export function applyTermFixes(text) {
  if (!text) return text;
  let out = text;
  for (const [re, repl] of TERM_FIXES) {
    out = typeof repl === 'function' ? out.replace(re, repl) : out.replace(re, repl);
  }
  return out.trim();
}

export function isMostlyRussian(text) {
  if (!text?.trim()) return true;
  const cyrillic = (text.match(/[\u0400-\u04FF]/g) || []).length;
  const latin = (text.match(/[a-zA-Z]/g) || []).length;
  const cjk = (text.match(/[\u3040-\u30FF\u4E00-\u9FFF\uAC00-\uD7AF]/g) || []).length;
  if (cyrillic === 0 && latin === 0 && cjk === 0) return true;
  return cyrillic >= latin && cyrillic >= cjk;
}
